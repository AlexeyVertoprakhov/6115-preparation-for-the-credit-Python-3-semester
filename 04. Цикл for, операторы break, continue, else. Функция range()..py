"""Цикл for – списочный. Т.е. операции в цикле выполняются для всех переменных, удовлетворяющих условию"""
for x in range (8):
	print(x)
"""Тут мы напечатали числа от 0 (что важно!) до 7 (что тоже важно!).
Говоря, про range(start, stop, step). Перебирает все значение на [start; stop) с шагом step."""
